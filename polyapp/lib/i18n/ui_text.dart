String trTextByCode(String localeCode, String ru, String en) {
  final code = localeCode.toLowerCase();
  if (code.startsWith('ru')) return ru;
  return translateEnglishUi(code, en);
}

String translateEnglishUi(String localeCode, String en) {
  final code = localeCode.toLowerCase();
  if (code.startsWith('en')) return en;
  final normalized = en.trim();
  if (normalized.isEmpty) return en;
  final exact = _exactByLocale[code]?[normalized];
  if (exact != null && exact.isNotEmpty) return exact;
  return _wordByWord(code, en);
}

String _wordByWord(String code, String input) {
  final dict = _wordByLocale[code];
  if (dict == null || dict.isEmpty) return input;
  final pattern = RegExp(r'[A-Za-z]+|[^A-Za-z]+');
  final out = StringBuffer();
  for (final match in pattern.allMatches(input)) {
    final token = match.group(0) ?? '';
    if (RegExp(r'^[A-Za-z]+$').hasMatch(token)) {
      final lower = token.toLowerCase();
      String? translated = dict[lower];
      if (translated == null && lower.endsWith('s')) {
        translated = dict[lower.substring(0, lower.length - 1)];
      }
      if (translated == null) {
        out.write(token);
      } else {
        out.write(translated);
      }
    } else {
      out.write(token);
    }
  }
  return out.toString();
}

